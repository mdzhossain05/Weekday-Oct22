package samplePackage;

public class DataType {

	public static void main(String[] args) {
		
		//any name, your phone number, weather, address, email
		
		
		//String - zakir.hossain1021@itsurface.com
		//int (integer) - 10, -50, 25446
		//double - 20.355, 15151.05, 10.0
		//boolean - true or false
		
	//	Data type		variable		value
		String 			firstName = 	"zakir";
		String lastName = "hossain";
		
		System.out.println(firstName);
		System.out.println(lastName);
		
	//	Data type		variable	value
		int 			numberOne = 100;
		int numberTwo = -300;
		
		System.out.println(numberOne);
		System.out.println(numberTwo);
		
		double weather = 100.20;
		double temp = 10;
		
		System.out.println(weather);
		System.out.println(temp);
		
		boolean raining = false;
		
		System.out.println(raining);
		
		
	}

}
